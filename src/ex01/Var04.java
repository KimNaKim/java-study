package ex01;

class Dog {
    //클래스 자료형
    static String name = "김산마루";
    static int age = 7;
    static String color = "황토색";
}

public class Var04 {
    public static void main(String[] args) {
        System.out.println(Dog.name);
        System.out.println(Dog.age);
        System.out.println(Dog.color);
    }
}
